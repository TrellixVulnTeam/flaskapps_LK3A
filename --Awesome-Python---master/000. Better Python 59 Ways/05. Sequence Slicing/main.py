# 이 예제 코드를 적는 필자는 파이썬을 처음 접했을 때 리스트같은 시퀀스 자료형의 슬라이싱에 대해 알게 되어 충격을 받았었다.
# 슬라이싱을 간단하게 설명하면 문자열을 원하는 부분만 자르는 것이다.

# 예시로 간단한 리스트를 만들어보자
a = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h']

# 슬라이싱의 기본 형태는 list[start:end]이고 start 인덱스는 포함되고 end 인덱스는 제외된다.
print("a[0:4]:", a[0:4])
print("a[4:8]:", a[4:8])

# 리스트의 처음부터 슬라이스할 때는 list[0:x]에서 0을 생략할 수 있다.
print("a[:4]:", a[:4])

# 리스트의 끝까지 슬라이스할 때는 list[x:len(list)]에서 len(list)를 생략할 수 있다.
print("a[10:len(a)]:", a[10:len(a)])
print("a[10:]:", a[10:])

# 슬라이싱에 start와 end에는 음수가 들어갈 수 있으며 뒤에서부터 인덱스를 센다.
print("a[-5:]:", a[-5:])
print("a[:-5]:", a[:-5])

# 슬라이싱의 결과는 완전히 새로운 리스트다.
b = a[:]
print("id(a):", id(a))
print("id(b):", id(b))

