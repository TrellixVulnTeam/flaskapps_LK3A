# 리스트와 튜플은 Python이 강력한 표현력을 가질 수 있도록 한 빌트인 타입
# 자료구조의 Array 개념과 비슷한 형태의 컨테이너이며,
# Python의 데이터 모델로 따지면 Iterable(반복 가능한), Sequence(순서가 있는) 특징을 가진 컨테이너 객체

# -- List
[1, 2, 3, 4, 5, 6]
# [v, v, ...]

# -- Tuple
(1, 2, 3, 4, 5, 6)
# (v, v, ...)
1, 2, 3, 4, 5, 6
# 별도로 감싸는 괄호를 명시하지 않아도, comma로 분리된 요소의 나열이 있다면 Tuple로 처리됨
(1,)
1,
# 값이 하나 뿐인 튜플은 요소 뒤에 comma를 붙여 주어야 함(요소만 있으면, 값 자체로 인식되므로)
